a = 15
b = 4

# Arithmetic
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Power:", a ** b)

# Comparison
print("Is a > b?", a > b)

# Logical
print("Logical AND:", a > 10 and b < 5)